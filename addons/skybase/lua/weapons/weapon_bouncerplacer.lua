function SWEP:Deploy()
	if self:Clip1() != self.Primary.ClipSize then
		self:Reload()
	else
		self:SendWeaponAnim(ACT_VM_DRAW)
	end
	return true
end
     
function SWEP:SecondaryAttack()
	return false
end    
     

     
function SWEP:PrimaryAttack()
	self:Grenade(self.Owner)
	self.Weapon:SetNextPrimaryFire(CurTime())
end
     
function SWEP:Grenade(ply)
	if SERVER then
		if !IsValid(self.Owner) then return end
		local tr = util.TraceHull({start = self.Owner:GetShootPos(),endpos = self.Owner:GetShootPos() + self.Owner:GetAimVector() * 60,maxs = 1,mins = 1,filter = { self.Owner, self.Weapon }})
		if tr.HitWorld and self:Clip1() == self.Primary.ClipSize then
			local grenade = ents.Create("bouncingmortar")
			grenadeang = tr.HitNormal:Angle()
			grenadeang.p = grenadeang.p + 90
			grenadepos = tr.HitPos + (tr.HitNormal * -10)
			grenade:SetPos(grenadepos)
			grenade:SetSkin(2)
			grenade:SetAngles(grenadeang)
			grenade:SetOwner(self.Owner)
			grenade:Spawn()
			constraint.Weld(grenade,game.GetWorld(),0,0,0,0,0)
			self:TakePrimaryAmmo(1)
			self:EmitSound("buttons/lever8.wav")
			self:SendWeaponAnim(ACT_VM_SECONDARYATTACK)
			self.Owner:GetViewModel():SetPlaybackRate(2)
			if self.Owner:GetAmmoCount(self.Primary.Ammo) == 0 then
				SafeRemoveEntityDelayed(self, 0.4) 
			else
				timer.Simple(0.3, function()
				if IsValid(self) and IsValid(self.Owner) then
				self:Reload()
				end
				end)
			end
		end
	end
end



	

function SWEP:Reload()
	if self.ReloadingTime and CurTime() <= self.ReloadingTime then return end
	if ( self:Clip1() < self.Primary.ClipSize and self.Owner:GetAmmoCount( self.Primary.Ammo ) > 0 ) then
		self:DefaultReload( ACT_VM_DRAW )
		local AnimationTime = self.Owner:GetViewModel():SequenceDuration()
		self.ReloadingTime = CurTime() + AnimationTime
		self:SetNextPrimaryFire(CurTime() + AnimationTime)
		self:SetNextSecondaryFire(CurTime() + AnimationTime)
	end
end